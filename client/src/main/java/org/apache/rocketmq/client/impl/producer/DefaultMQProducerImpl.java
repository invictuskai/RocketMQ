/**
 * 通过Request方法发送的消息，生产者现车个在这个Request发送之后会挂起等待，等到消费者消费完这条消息之后要给对应的生产者发送一个响应消息，响应消息被生产者收到之后对应的生产者线程会恢复继续运行。
 * 生产者msg添加了额外信息，客户端ID，关联ID。
 * 发送到broker之后，消费者从broker拿到这条消息检查消息类型发现是一个需要发送响应的request消息，消费完之后根据msg的关联id，客户端id生成一个相应消息，发送给brrker
 * broker拿到这条响应消息之后根据客户端id找到对应的Channel，将响应消息发送给生产者。
 * 生产者这把拿到响应消息之后读取出来关联id找到对应的requestFuture，将对应阻塞的线程唤醒，相当于模拟了一次RPC调用过程。
 */
